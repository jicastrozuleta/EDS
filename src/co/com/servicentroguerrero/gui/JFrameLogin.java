    private void iniciarLiquidacion(final Empleado empleadoLiquidador) {

        String mensaje = "<html>"
                + "<h3>"
                + "<b>Bienvenido " + empleadoLiquidador.getNombres() + " " + empleadoLiquidador.getApellidos()
                + "</b></h3><p><br>Recuerde que todo cambio generado en la liquidacion queda registrado a "
                + "su nombre.<br><b>Si no es su nombre por favor cierre esta ventana o seleccione cancelar.</b>"
                + "<br>Si es correcto seleccione aceptar.</b></p></html>";
        int opcion = JOptionPane.showConfirmDialog(this, mensaje, "¿Nombre Correcto?", JOptionPane.CANCEL_OPTION);
        if (opcion == 0) {
            JFrameLiquidacion jFrameLiquidacion = new JFrameLiquidacion(empleadoLiquidador);
            jFrameLiquidacion.setLocationRelativeTo(this);
            jFrameLiquidacion.setVisible(true);
            this.setVisible(false);
        }
    }
