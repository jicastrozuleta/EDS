    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        try {
            String user = jTextFieldUser.getText().trim();
            String pass = "";
            char[] cPass = jPasswordFieldPassword.getPassword();
           
            /*cargar la contraseña ingresada*/
            for (int i = 0; i < cPass.length; i++) {
                pass += cPass[i];
                cPass[i] = 0;
            }
            /*limpiar contraseña*/
            jPasswordFieldPassword.setText("");
            
            /*cargar el empleado liquidador*/
            Empleado empleado = ControllerBO.login(user, pass);
            
            /*Si el empleado es correcto iniciar la liquidacion*/
            if (empleado != null) {
                iniciarLiquidacion(empleado);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

