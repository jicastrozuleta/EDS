    private void jComboBoxCombustiblesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxCombustiblesActionPerformed
        int index = jComboBoxCombustibles.getSelectedIndex();
        if(index != POSITION_DEFAULT){
            jLabelPrecioActual.setText(listaCombustibles.get(index - 1).getCombustible());
            jTextFieldPrecioActual.setText("$" + Util.formatearMiles(listaCombustibles.get(index -1).getPrecioVigente()));
        }
        else {
            jLabelPrecioActual.setText("Combustible");
            jTextFieldPrecioActual.setText("");
        }
    }//GEN-LAST:event_jComboBoxCombustiblesActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int index = jComboBoxCombustibles.getSelectedIndex();
        if(index != POSITION_DEFAULT){
            String sPrecio = jTextFieldnuevoPrecio.getText().trim();
            if(sPrecio.length() > 0 && Util.isNumeric(sPrecio)){
                /*verificar la actualizacion del precio*/
                actualizarNuevoPrecio(index - 1, Double.parseDouble(sPrecio));
            }
            else {
                JOptionPane.showMessageDialog(this, "Ingrese un nuevo precio.", "Error", JOptionPane.ERROR_MESSAGE);
                jTextFieldnuevoPrecio.requestFocusInWindow();
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Selecione un combustible", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    
    
    /**
     * CArgar los combustibles disponibles para actualizar precio.
     */
    private void cargarCombustiblesDisponibles() {
         /*Marcar posicion por defecto y asignarla al combo*/
        jComboBoxCombustibles.insertItemAt("Seleccione Combustible - ", POSITION_DEFAULT);
        jComboBoxCombustibles.setSelectedIndex(POSITION_DEFAULT);

        /*Agregar los items con los datos de los empleados con rol islero.*/
        ControllerBO.cargarCombustibles().forEach((c) -> {
            getListaCombustibles().add(c);
        });
        
        /*Llenar el combobox con los combustibles disponibles*/
        listaCombustibles.forEach((combustible) -> {
            jComboBoxCombustibles.addItem(combustible.getCombustible());
        });        
    }

    
    
    public ArrayList<Combustible> getListaCombustibles() {
        return listaCombustibles;
    }

    private void actualizarNuevoPrecio(final int index, final Double nuevoPrecio) {
        try {
            int option = JOptionPane.showConfirmDialog(this, "Aceptar para actualizar el precio", "Â¿Actualizar Precio?", JOptionPane.OK_CANCEL_OPTION);
            if (option == ACEPTAR) {
                if(ControllerBO.actualizarPrecioCombustible(listaCombustibles.get(index).getIdCombustible(), nuevoPrecio)){
                    JOptionPane.showMessageDialog(this, "Precio actualizado.", "Correcto", JOptionPane.INFORMATION_MESSAGE);
                    JFrameActualizarPrecios.this.dispose();
                    this.refresh.refrescarVentanaLiquidacion();
                }      
            }
        } catch (Exception ex) {
            Logger.getLogger(JFrameActualizarPrecios.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }

