    private void jTextFieldCincuentaMilFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldCincuentaMilFocusLost
        // TODO add your handling code here:
        String text = jTextFieldCincuentaMil.getText().trim();
        /*Si contiene texto iniciar calculo de monto de denominacion */
        if(text != null && text.length() > 0){
            if(Util.isNumeric(text)){
                this.totalCincuentaMil = Util.calcularMontoPorDenominacion(Integer.parseInt(text), CINCUENTA_MIL);
                jLabelCincuentaMil.setText("$" + Util.formatearMiles(this.totalCincuentaMil));
            }
        }
        else {
            jLabelCincuentaMil.setText("$" + Util.formatearMiles(0));
            this.totalCincuentaMil = 0;
        }
        calcularTotalDinero();
    }//GEN-LAST:event_jTextFieldCincuentaMilFocusLost

