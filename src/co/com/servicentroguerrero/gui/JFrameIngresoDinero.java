    /**
     * Calcular el dinero ingresado y mostrarlo en pantalla.
     */
    private void calcularTotalDinero(){
           this.totalDinero =   this.totalCienMil +
                                this.totalCincuentaMil +
                                this.totalVeinteMil +
                                this.totalDiezMil +
                                this.totalCincoMil +
                                this.totalDosMil +
                                this.totalMil +
                                this.totalBauches +
                                this.totalVales +
                                this.totalMonedas +
                                this.totalEfectivos;
           jLabelTotalDineroRecibido.setText("$" + Util.formatearMiles(this.totalDinero));
    }

    
    /**
     * Get total de dinero ingresado.
     * @return 
     */
    public double getTotalDinero() {
        return totalDinero;
    }
    
    
